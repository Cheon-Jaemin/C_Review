#include <stdio.h>

struct student
{
	int num;
	char name[10];
	double grade;
};

void printStudent()
{
	struct student s;
	
	printf("학번을 입력하시오. : ");
	scanf("%d", &s.num);

	printf("이름을 입력하시오. : ");
	scanf("%s", s.name);

	printf("학점을 입력하시오. : ");
	scanf("%lf", &s.grade);

	printf("학번 : %d\n", s.num);
	printf("이름 : %s\n", s.name);
	printf("학점 : %lf\n", s.grade);
}

int main(void)
{
	printStudent();
	return 0;
}
